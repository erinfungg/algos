var threeSum = function(arr) {
  arr.sort((a, b) => a - b);
  let hash = {};
  let triplets = [];
  for (let i = 0; i < arr.length; i++) {
    let curr = arr[i];
    let left = i + 1;
    let right = arr.length - 1;

    while (left < right) {
      let sum = curr + arr[left] + arr[right];
      if (sum < 0) left++;
      else if (sum > 0) right--;
      else {
        if (!hash[[curr, arr[left], arr[right]]])
          hash[[curr, arr[left], arr[right]]] = [curr, arr[left], arr[right]];
        //   console.log(hash)
        // triplets.push([curr, arr[left], arr[right]]);
        // console.log(triplets)
        left++;
        right--;
      }
    }
  }
  for (let key in hash) {
    triplets.push(hash[key]);
  }
  return triplets;
};

